ll power(ll x, ll y){
    ll res = 1;
    while(y > 0){
        if(y & 1) res = (res * x) % nod;
        y >>= 1;
        x = (x * x) % nod;
    }
    return (res % nod);
}


ll nodes;
vll adj[mx], arr(mx, 0), sum(mx, 0), subTreeSize(mx, 0);
ll ans;
void dfs(ll curr) {
    sum[curr] += arr[curr];
    if(adj[curr].empty()) {
        subTreeSize[curr] = 1;
    }

    for(auto child : adj[curr]) {
        dfs(child);
        sum[curr] += sum[child];
        subTreeSize[curr] += subTreeSize[child];
    }

    ans = max(ans, (ll)ceil(1.0 * sum[curr] / subTreeSize[curr]));
}

