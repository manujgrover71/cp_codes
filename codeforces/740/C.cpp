ll power(int x, unsigned int y){
    ll res = 1;
    while(y > 0){
        if(y & 1) res = res * x;
        y >>= 1;
        x *= x;
    }
    return res;
}

void solve(){
    int n, q;
    cin >> n >> q;
    ll len = 1e8;
    while(q--){
        int l, r;
        cin >> l >> r;
        len = min(len, (ll)(r - l) + 1);
    }
    cout << len << '\n';
    for(int i = 0; i < n; i++){
        cout << (i % len) << ' ';
    }
}

